/**
 * Danh sách màu gradient cho biodigital cards
 */
const GRADIENT_COLORS = [
  '#FF6B9D', // Pink
  '#FFB347', // Orange
  '#FFFF66', // Yellow
  '#66FF66', // Green
  '#66B3FF', // Blue
  '#B366FF', // PurpleBlue
  '#555555', // Purple
];

/**
 * Lấy màu gradient random dựa trên title (để đảm bảo cùng title luôn có cùng màu)
 */
export function getRandomGradientColor(title: string): string {
  if (!title) return GRADIENT_COLORS[0];
  
  // Tạo hash từ title để đảm bảo cùng title luôn có cùng màu
  let hash = 0;
  for (let i = 0; i < title.length; i++) {
    hash = title.charCodeAt(i) + ((hash << 5) - hash);
  }
  
  // Lấy index từ hash
  const index = Math.abs(hash) % GRADIENT_COLORS.length;
  return GRADIENT_COLORS[index];
}

