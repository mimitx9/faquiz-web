/**
 * Match code của category: match chính xác hoặc match dạng {keyword}-*
 * Ví dụ: tìm "GiaiPhau" sẽ match với "GiaiPhau", "GiaiPhau-HMU", "GiaiPhau-YDN", "GiaiPhau-TUMP"
 */
export function matchesCategoryCode(code: string, keyword: string): boolean {
  if (!code || !keyword) return false;
  
  const normalizedCode = normalizeSearchKeyword(code);
  const normalizedKeyword = normalizeSearchKeyword(keyword);
  
  // Match chính xác hoặc match dạng {keyword}-*
  return normalizedCode === normalizedKeyword || normalizedCode.startsWith(`${normalizedKeyword}-`);
}

/**
 * Match title của category với wild card matching
 */
export function matchesCategoryTitle(title: string, keyword: string): boolean {
  if (!title || !keyword) return false;
  
  const normalizedTitle = normalizeSearchKeyword(title);
  const normalizedKeyword = normalizeSearchKeyword(keyword);
  
  // Kiểm tra xem title có chứa keyword không
  return normalizedTitle.includes(normalizedKeyword);
}

/**
 * Convert hex color sang rgba với opacity
 */
export function hexToRgba(hex: string, alpha: number = 1): string {
  if (!hex) return `rgba(0, 0, 0, ${alpha})`;
  
  // Loại bỏ dấu # nếu có
  const cleanHex = hex.replace('#', '');
  
  // Parse hex thành RGB
  const r = parseInt(cleanHex.substring(0, 2), 16);
  const g = parseInt(cleanHex.substring(2, 4), 16);
  const b = parseInt(cleanHex.substring(4, 6), 16);
  
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

